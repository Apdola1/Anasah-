// أدوات الكلمات العربية + بنك كلمات لنمط السباق
// كل كلمة في البنك ٥ حروف بعد التطبيع.

// إزالة التشكيل والتطويل (حركات + تنوين + مدّ + تطويل)
const TASHKEEL = /[ؐ-ًؚ-ٰٟۖ-ۭـ]/g

// تطبيع الحروف عشان المطابقة تكون متسامحة:
// الهمزات → ا ، التاء المربوطة → ه ، الألف المقصورة → ي ، ؤ → و ، ئ → ي
export function normalize(s) {
  return (s || '')
    .replace(TASHKEEL, '')
    .replace(/[أإآٱ]/g, 'ا') // أ إ آ ٱ → ا
    .replace(/ة/g, 'ه')                     // ة → ه
    .replace(/[ىی]/g, 'ي')             // ى ی → ي
    .replace(/ؤ/g, 'و')                     // ؤ → و
    .replace(/ئ/g, 'ي')                     // ئ → ي
    .trim()
}

// حروف الكلمة بعد التطبيع (مصفوفة أحرف عربية فقط)
export function toLetters(s) {
  return normalize(s)
    .split('')
    .filter((ch) => /[ء-ي]/.test(ch))
}

export const WORD_LEN = 5

export function isValidLength(word) {
  return toLetters(word).length === WORD_LEN
}

